// Utility functions for emoji management
export const getRecentEmojis = (): string[] => {
  if (typeof window === "undefined") return [];
  const recent = localStorage.getItem("recent-emojis");
  return recent ? JSON.parse(recent) : [];
};

export const addRecentEmoji = (emoji: string): void => {
  if (typeof window === "undefined") return;

  const recent = getRecentEmojis();
  const filtered = recent.filter((e) => e !== emoji);
  const updated = [emoji, ...filtered].slice(0, 32); // Keep only 32 recent emojis

  localStorage.setItem("recent-emojis", JSON.stringify(updated));
};

